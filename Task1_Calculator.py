a = int(input("Enter the A Value \n\t"))
b = int(input("Enter the B Value \n\t"))
operation = input(
    "add/sub/mul/div \t\n")

if(operation=="add"):
   print(a+b)
elif(operation=="sub"):
    print(a-b)
elif(operation=="mul"):
    print(a*b)
elif(operation=="div"):
    print(a/b)


         
